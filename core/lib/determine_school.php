function determineSchoolObject() {
    $user = \lms_steam::get_current_user();
    $data = $user->get_attribute("ldap:dn");

    $lspbSchoolID = "163621";
    $lspbGSID = "188542";
    $lspbOthersID = "165002";
    $whichSystem = "";
    $schools = "";
     
    // traverse ldap string
    $dataArray = explode(",", $data);
    for ($count = count($dataArray) - 1; $count > 0; $count--) {
        $dataElement = explode("=", $dataArray[$count]);
        // look at the "ou" elements
        if ($dataElement[0] == "ou") {
            if ($dataElement[1] == "bidowl") {
                $whichSystem = "bid";
            } else if ($dataElement[1] == "lernstatt") {
                $whichSystem = "lernstatt";
            } else {
                if ($whichSystem === "lernstatt") {
                    $folder = \steam_factory::get_object($GLOBALS["STEAM"]->get_id(), $lspbSchoolID);
                    $school = \steam_factory::get_object_by_name($GLOBALS["STEAM"]->get_id(), $folder->get_path() . "/" . $dataElement[1]);
                    if ($school instanceof \steam_room) {
                        return $school;
                    } else {
                        $folder = \steam_factory::get_object($GLOBALS["STEAM"]->get_id(), $lspbGSID);
                        $school = \steam_factory::get_object_by_name($GLOBALS["STEAM"]->get_id(), $folder->get_path() . "/" . $dataElement[1]);
                        if ($school instanceof \steam_room) {
                            return $school;
                        } else {
                            $folder = \steam_factory::get_object($GLOBALS["STEAM"]->get_id(), $lspbOthersID);
                            $school = \steam_factory::get_object_by_name($GLOBALS["STEAM"]->get_id(), $folder->get_path() . "/" . $dataElement[1]);
                            if ($school instanceof \steam_room) return $school;
                        }
                    }
                    break;
                } else if ($whichSystem === "bid") {
                    if (!is_array($schools)) {
                        $schools = initializeBidSchools();
                    }
                    if (isset($schools[$dataElement[1]])) {
                        $school = \steam_factory::get_object_by_name($GLOBALS["STEAM"]->get_id(), $schools[$dataElement[1]]);
                        if ($school instanceof \steam_room) return $school;
                    }
                }
            }
        }
    }
    return "";
}
