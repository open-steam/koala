<h1>Philosophie</h1>

<p>Der Server <i><?php echo $config_server_name ?></i> ist in erster Linie ein Kooperationsforum. Jeder Projektgruppe wird ein in sich abgeschlossener Arbeitsraum zur Verfügung gestellt, in dem ungestört und unbeobachtet kommuniziert und gearbeitet werden kann. Eine einfache, benutzerorientierte Verwaltung organisiert die Gruppenzugehörigkeit und die Rechtevergabe für jedes Dokument. Alle gängigen Dateiformate lassen sich einbinden.</p>

